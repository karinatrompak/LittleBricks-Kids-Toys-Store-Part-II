<h2>Politica de Privacidade</h2>
1. A responsável pela recolha e pelo tratamento dos seus dados pessoais é David Gomes e Karina Trompak, que lhe presta todo o apoio relativamente à compra de produtos e que decide quais os dados recolhidos, meios e finalidades para os quais os dados são utilizados. <br>
2. Apenas retemos as informações pelo tempo necessário para fornecer o serviço ou informação. Quando armazenamos dados, protegemos dentro dos meios comercialmente aceitáveis para evitar perdas e roubos, bem como acesso, divulgação, cópia, uso ou modificação não autorizadas. <br>
3. A proteção da privacidade dos dados pessoais é um compromisso fundamental nosso para com o titular dos dados pessoais que utiliza as nossas plataformas digitais e serviços. A presente política de privacidade serve para explicar quais os dados pessoais que recolhemos, para que finalidades os usamos, com os tratamos, com quem os partilhamos, durante quanto tempo os conservamos, bem como as formas de entrar em contacto a fim de exercer os seus. <br>
4. Somos responsáveis pela monitorização da conformidade do tratamento com as mesmas aplicáveis, na prestação de esclarecimento com a Comissão Nacional de Proteção de Dados. <br>
5. Não compartilhamos informações de identificação pessoal publicamente ou com terceiros, exceto quando nos for exigido por lei. <br>
6. Os dados pessoais tratados incluem: nome, morada, endereços de correio eletrónico, números de contacto, documentos de identificação civil e fiscal, dados de faturação, dados bancários, dados sobre visitas do cliente ao website ou das comunicações eletrónicas. <br>
7. A nossa Política de Privacidade, aborda determinados direitos que o cliente pode exercer, entre eles, o Direito a retirar o consentimento, isto é, a qualquer momento, o cliente pode opor-se ou solicitar que seja retirado o seu consentimento. <br>
8. Os dados pessoais poderão ser tratados para efeitos de cumprimento de obrigações legais e para fins de investigação e deteção de crimes  Poderão ser utilizados para outras finalidades, tais como, envio de reclamações e sugestões, conhecer novos produtos. <br>
9. Os dados serão armazenados e conservados apenas pelo período mínimo necessário para a prossecução das finalidades que motivaram a sua recolha.<br>
10. Respeitamos a privacidade de todos os utilizadores do seu website,e compromete-se a proteger as informações pessoais que cada utilizador decidir partilhar, nos termos da legislação nacional e comunitária.<br>
