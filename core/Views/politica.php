<h5>Direitos do Titular de Dados Pessoais</h5>
O titular de dados pessoais pode exercer os seguintes direitos: <br>

 - Direito à informação: Esta explícita de forma clara a sua política de privacidade, no momento de qualquer serviço; <br>

 - Direito ao acesso e à retificação: Pode aceder e editar diretamente de forma autónoma os seus dados, seguindo o link disponível, após cada mensagem que recebe do prestador de serviços; <br>

 - Direito de limitar, ou opor ao tratamento dos seus dados: Tem em cada mensagem um link com a possibilidade de limitar/opor; <br>

 - Direito ao esquecimento: Deverá solicitar a remoção total dos seus dados, enviando uma mensagem o nosso email com a sua identificação e um pedido de remoção. <br>
 <br>
<h5>Transmissão de Dados Pessoais</h5>
Poderão ser transferidos dados para um país fora da UE com o objetivo de melhorar o funcionamento das suas plataformas digitais e/ou a prestação dos seus serviços. Nesses casos, serão assegurados que as transferências de dados se realizam no estrito cumprimento das normas legais aplicáveis. <br>
Sempre que aplicável, os seus dados pessoais serão também transferidos para o parceiro responsável pelo processamento de cada uma das suas encomendas e sobre os quais declinamos qualquer responsabilidade. <br> <br>

<h5>Segurança dos Dados Pessoais</h5>
O website está elaborado de forma que sempre que seja recolhida ou utilizada informação pessoal, os dados sejam encriptados através do protocolo SSL (Secure Socket Layer). Esta tecnologia protege os dados, nomeadamente da interceção fraudulenta e como tal, minimiza o risco associado. <br>
<br>
<h5>Conservação dos Dados Pessoais</h5>
A lei determina a conservação dos dados por um período mínimo, designadamente: <br>

. por um ano, relativamente a dados de tráfego de localização, isto para efeitos de investigação, deteção e repressão de crime grave. <br>

. por dez anos, relativamente a dados necessários para informações fornecidas à Autoridade Tributária. <br>

A empresa conserva os seus dados pessoais, sempre de acordo com a lei, as orientações e as decisões da CNPD ou consoante o que for aplicável, até que exerça o seu direito de oposição, direito a ser esquecido, ou que revogue o seu consentimento. Contudo serão sempre observáveis todas as obrigações legais e contratuais que definem esses períodos. <br><br>
<h5>Alterações na Política de Privacidade</h5>
A presente Política de Privacidade poderá ser atualizada a qualquer momento, de acordo com as alterações legais que possam ocorrer, com o objetivo de proteger o cliente, mantendo os seus dados seguros e acessíveis.
<br><br>

