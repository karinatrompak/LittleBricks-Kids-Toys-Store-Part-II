<h5>Lei e Foro</h5>
1. As presentes condições gerais regem-se pela lei portuguesa. <br>
2. Todos os litígios emergentes da interpretação ou execução do presente acordo serão dirimidos pelo Tribunal Judicial da Comarca da Póvoa de Varzim, com expressa renúncia a qualquer outro. <br>
<br>
<h5>Resolução em Alternativa de litígios</h5>
Em caso de litígio, o consumidor pode recorrer a uma das entidades de Resolução Alternativa de Litígios de Consumo. <br>
Para mais informações, deverá consultar o Portal do Consumidor (www.consumidor.pt) <br>
No cumprimento da legislação em vigor, esta indica que, em casa delitígio, o cliente pode recorrer a uma entidade de Resolução Alternativa de Litígios de Consumo: <br>
        - CNIACC - Centro Nacional de Informação e Arbitragem de Conflitos de Consumo(cniacc.pt) <br>
        - CIAB - Centro de Informação, mediação e Arbitragem de Consumo (ciab.pt) <br>
